    private void bt_excluir_CliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluir_CliActionPerformed
        if(javax.swing.JOptionPane.showConfirmDialog(null,"Deseja Excluir o cliente selecionado?","ATENÇÂO ",javax.swing.JOptionPane.YES_NO_OPTION )==0)
        {
        try{
        int index = tblresultado.getSelectedRow();
        objCliente.setCodigo(clientes.get(index).getCodigo());
        objClienteDAO.excluir(objCliente);
        JOptionPane.showMessageDialog(this, "Registro excluido com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        clientes.remove(index);
        modelo.removeRow(index);
        }
        
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Registro não selecionado!", "Erro:", JOptionPane.INFORMATION_MESSAGE);
        }
        }
        
    }//GEN-LAST:event_bt_excluir_CliActionPerformed

