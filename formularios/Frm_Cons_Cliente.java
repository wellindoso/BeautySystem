    private void bt_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pesquisarActionPerformed
       if (!tb_txtPesquisa.getText().equals("")){
            if(rb_cliBloq.isSelected()&& rb_cliNome.isSelected())
            {
                clientes = objClienteDAO.consultar("Nome", tb_txtPesquisa.getText(), 1);
                if (clientes.size()>0){
                    modelo = (DefaultTableModel)tblresultado.getModel();
                    while(modelo.getRowCount()>0){
                        modelo.removeRow(0);
                    }
                    clientes.stream().forEach((p) -> {
                        modelo.addRow(new Object[] {p.getCodigo(),p.getNome(), p.getCpf()});
                    });
                }
                else {
                    JOptionPane.showMessageDialog(this, "Nenhum registro encontrado!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else if(rb_cliDesbloq.isSelected()&& rb_cliNome.isSelected())
            {
                clientes = objClienteDAO.consultar("Nome", tb_txtPesquisa.getText(), 2);
                if (clientes.size()>0){
                    modelo = (DefaultTableModel)tblresultado.getModel();
                    while(modelo.getRowCount()>0){
                        modelo.removeRow(0);
                    }
                    clientes.stream().forEach((p) -> {
                        modelo.addRow(new Object[] {p.getCodigo(),p.getNome(), p.getCpf()});
                    });
                }
                else {
                    JOptionPane.showMessageDialog(this, "Nenhum registro encontrado!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else if(rb_cliBloq.isSelected()&& rb_cliCodigo.isSelected())
            {
                clientes = objClienteDAO.consultar("IdCliente", tb_txtPesquisa.getText(), 1);
                if (clientes.size()>0){
                    modelo = (DefaultTableModel)tblresultado.getModel();
                    while(modelo.getRowCount()>0){
                        modelo.removeRow(0);
                    }
                    clientes.stream().forEach((p) -> {
                        modelo.addRow(new Object[] {p.getCodigo(),p.getNome(), p.getCpf()});
                    });
                }
                else {
                    JOptionPane.showMessageDialog(this, "Nenhum registro encontrado!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else if(rb_cliDesbloq.isSelected()&& rb_cliCodigo.isSelected())
            {
                clientes = objClienteDAO.consultar("IdCliente", tb_txtPesquisa.getText(), 2);
                if (clientes.size()>0){
                    modelo = (DefaultTableModel)tblresultado.getModel();
                    while(modelo.getRowCount()>0){
                        modelo.removeRow(0);
                    }
                    clientes.stream().forEach((p) -> {
                        modelo.addRow(new Object[] {p.getCodigo(),p.getNome(), p.getCpf()});
                    });
                }
                else {
                    JOptionPane.showMessageDialog(this, "Nenhum registro encontrado!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                }
            }
           // TODO add your handling code here:
        }
        else {
            JOptionPane.showMessageDialog(this, "Informe o valor da pesquisa!", "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bt_pesquisarActionPerformed

