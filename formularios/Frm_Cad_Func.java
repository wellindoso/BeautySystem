     private boolean validarCampos(){
        return (!tb_bairroFunc.getText().equals("")) &&
                (!tb_celularFunc.getText().equals("")) &&
                (!tb_cepFunc.getText().equals("")) &&
                (!tb_cidadeFunc.getText().equals("")) &&
                (!tb_cpfFunc.getText().equals("")) &&
                (!tb_logradouroFunc.getText().equals("")) &&
                (!tb_nomeDeUsuarioFunc.getText().equals("")) &&
                (!tb_nomeFunc.getText().equals("")) &&
                (!tb_numFunc.getText().equals("")) &&
                (!tb_telefoneFunc.getText().equals(""))&&
                (!tb_rgFunc.getText().equals(""));
              }
