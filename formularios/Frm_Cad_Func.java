     private void popularAtributos(){
        objFuncionario.setNome(tb_nomeFunc.getText());
        objFuncionario.setCpf(tb_cpfFunc.getText());
        objFuncionario.setRg(tb_rgFunc.getText());
        objFuncionario.setLogradouro(tb_logradouroFunc.getText());
        objFuncionario.setNumero(Integer.parseInt(tb_numFunc.getText()));
        objFuncionario.setCep(tb_cepFunc.getText());
        objFuncionario.setBairro(tb_bairroFunc.getText());
        objFuncionario.setCelular(tb_celularFunc.getText());
        objFuncionario.setTelefone(tb_telefoneFunc.getText());
        objFuncionario.setCidade(tb_cidadeFunc.getText());
        objFuncionario.setEstado(String.valueOf(cb_estadoFunc.getSelectedItem()));
        objFuncionario.setNivelAcesso(String.valueOf(cb_nivelAcessoFunc.getSelectedItem()));
        objFuncionario.setNomeUsuario(tb_nomeDeUsuarioFunc.getText());
        objFuncionario.setRepeticaoSenha(String.valueOf(tb_repeticaoSenhaFunc.getPassword()));
        objFuncionario.setSenhaUsuario(String.valueOf(tb_senhaFunc.getPassword()));
        
        
    }
    private void bt_InserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_InserirActionPerformed
      if(validarCampos()){
            try{
                popularAtributos();
                if(objFuncionario.getSenhaUsuario().equals(objFuncionario.getRepeticaoSenha()))
                {
                objFuncionarioDAO.gravar(objFuncionario);
                JOptionPane.showMessageDialog(this, "Registro inserido com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                dispose();
                } 
                else{
                  JOptionPane.showMessageDialog(this, "Repetição de Senha Inválida!", "Erro", JOptionPane.INFORMATION_MESSAGE); 
                 tb_senhaFunc.requestFocus();
                }
            } 
            catch (NumberFormatException | HeadlessException e){
                JOptionPane.showMessageDialog(this, "Erro"+e);
            }        // TODO add your handling code here:
        }
    }//GEN-LAST:event_bt_InserirActionPerformed

