     private void limparCampos()
     {
         tb_descricaoPro.setText("");
         tb_grupoPro.setText("");
         tb_valorVendaPro.setText("");
         tb_valorCompraPro.setText("");
     }
     
     private void cancelar(){  
        if(javax.swing.JOptionPane.showConfirmDialog(null,"Deseja Cancelar a Operacao?","ATENÇÂO ",javax.swing.JOptionPane.YES_NO_OPTION )==0){  
            this.dispose();  
        }
    }  
     
      private boolean validarCampos(){
        return (!tb_descricaoPro.getText().equals("")) &&
                (!tb_grupoPro.getText().equals("")) &&
                (!tb_valorCompraPro.getText().equals("")) &&
                (!tb_valorVendaPro.getText().equals(""));
                }
     
      private void popularAtributos() throws ParseException{
      SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        objProduto.setDescricao(tb_descricaoPro.getText());
        objProduto.setGrupoProdutos(Integer.valueOf(tb_grupoPro.getText()));
        objProduto.setValorCompra(Double.valueOf(tb_valorCompraPro.getText().replaceAll(",", ".")));
        objProduto.setValorVenda(Double.valueOf(tb_valorVendaPro.getText().replaceAll(",", ".")));
            java.sql.Date data1 = new java.sql.Date(formato.parse(stringData).getTime());  
        objProduto.setDataCadastro(data1);
      }
