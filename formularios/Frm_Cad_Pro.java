    private void tb_grupoProFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tb_grupoProFocusLost
     objGrupoProdutos = objGrupoProdutosDAO.consultar(Long.parseLong(tb_grupoPro.getText()));
     if(objGrupoProdutos.getDescricao() == null)
     {
         JOptionPane.showMessageDialog(this, "Nenhum registro encontrado!", "Informação", JOptionPane.INFORMATION_MESSAGE);
     }
     else{
     lb_descricaoGrupo.setText(objGrupoProdutos.getDescricao());
     bt_Inserir.requestFocus();
     }
    }//GEN-LAST:event_tb_grupoProFocusLost
    
