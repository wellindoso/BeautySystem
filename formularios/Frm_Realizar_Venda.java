    private void tb_clienteVendaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tb_clienteVendaFocusLost
        objCliente = objClienteDAO.consultar(Long.parseLong(tb_clienteVenda.getText()));
        if(objCliente.getNome()== null)
        {
            JOptionPane.showMessageDialog(this, "Nenhum registro encontrado!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            tb_clienteVenda.requestFocus();
            tb_clienteVenda.setText("");
            bt_inserir.setEnabled(false);
        }
        else if(objCliente.getSituacao().equals("Bloqueado"))
        {
            JOptionPane.showMessageDialog(this, "Cliente "+objCliente.getNome()+" Bloqueado!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            tb_clienteVenda.requestFocus();
            tb_clienteVenda.setText("");
            lb_nomeCliente.setText("");
            bt_inserir.setEnabled(false);
        }
        else{
        lb_nomeCliente.setText(objCliente.getNome());
        tb_FuncionarioVenda.requestFocus();
        objVenda.setCliente(objCliente);
        bt_inserir.setEnabled(true);
        }
    }//GEN-LAST:event_tb_clienteVendaFocusLost

