    private void bt_inserirItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_inserirItemActionPerformed
        if (tb_clienteVenda.getText().equals("") || tb_FuncionarioVenda.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Preencha os campos relacionados ao produto!", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            if(verificarEstoque())
            {
                if(Double.valueOf(tb_valorItem.getText().replace(",", "."))!= objProduto.getValorVenda())
            {
                objProduto.setValorVenda(Double.valueOf(tb_valorItem.getText().replace(",", ".")));
                objProdutoDAO.editar(objProduto);
            }
            objVenda.getProdutos().add(objProduto);
            modelo = (DefaultTableModel)tblprodutos.getModel();
            modelo.addRow(new Object[]{objProduto.getCodigo(), objProduto.getDescricao(), objProduto.getQuantidade(), objProduto.getValorVenda()});
            objVenda.setValorVenda(objVenda.getValorVenda()+(Integer.valueOf(tb_quantidade.getText())* Double.valueOf(tb_valorItem.getText().replace(",", "."))));
            lb_valorParcialVenda.setText(Double.toString(objVenda.getValorVenda()));
            JTab.setSelectedIndex(0);
            tb_codigoPro.setText("");
            tb_quantidade.setText("");
            tb_valorItem.setText("");
            lb_descricao.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Quantidade informada ("+tb_quantidade.getText()+") é maior do que estoque ("+objProduto.retornaEstoque()+")", "Informação", JOptionPane.INFORMATION_MESSAGE);
                tb_codigoPro.requestFocus();
            }
        }
    }//GEN-LAST:event_bt_inserirItemActionPerformed

