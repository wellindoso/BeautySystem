private void criaDiretorio(String novoDiretorio){  
    String nomeDiretorio = null;   
    String separador = java.io.File.separator;   
    try {       
         nomeDiretorio = "C:" + separador + novoDiretorio;   
         if (!new File(nomeDiretorio).exists()) { // Verifica se o diretório existe.   
             (new File(nomeDiretorio)).mkdir();   // Cria o diretório   
         }   
    } catch (Exception ex) {   
         JOptionPane.showMessageDialog(null,"Err","Erro ao criar o diretório" + ex.toString(),JOptionPane.ERROR_MESSAGE);   
    }  
}  
    private void gerarRelatorioVenda()
    {
        criaDiretorio("relatoriosBeautySystem");
        Document comprovanteVenda = new Document(PageSize.A4);
            try{
            PdfWriter.getInstance(comprovanteVenda, new FileOutputStream("C:\\relatoriosBeautySystem\\ComprovanteVenda.pdf"));
            comprovanteVenda.open();
            Paragraph paragrafo = new Paragraph("Comprovante de Venda");    
             paragrafo.setAlignment(Element.ALIGN_CENTER);
            comprovanteVenda.add(new Paragraph(paragrafo));
            Paragraph paragrafoNomeCli = new Paragraph("Nome do Cliente: "+objVenda.getCliente().getNome());    
             paragrafoNomeCli.setAlignment(Element.ALIGN_CENTER);
             comprovanteVenda.add(paragrafoNomeCli);
             Paragraph paragrafocodiVen = new Paragraph("Código da Venda: "+objVenda.getCodigo());    
             paragrafocodiVen.setAlignment(Element.ALIGN_CENTER);
             comprovanteVenda.add(paragrafocodiVen); 
             Paragraph paragrafodataVen = new Paragraph("Data da Venda: "+tb_dataVenda.getText());    
             paragrafodataVen.setAlignment(Element.ALIGN_CENTER);
             comprovanteVenda.add(paragrafodataVen);
             Paragraph paragrafonomeFun = new Paragraph("Nome do Funcionario: "+objVenda.getFuncionario().getNome());    
             paragrafonomeFun.setAlignment(Element.ALIGN_CENTER);
             comprovanteVenda.add(paragrafonomeFun);
             comprovanteVenda.add(new Paragraph("\n"));
             PdfPTable tabela = new PdfPTable(4);
             PdfPCell header = new PdfPCell(new Paragraph("Lista de Ítens da Venda"));
            header.setColspan(4);
            tabela.addCell(header);	
        tabela.addCell("Codigo Item");
        tabela.addCell("Descrição");
        tabela.addCell("Quantidade");
        tabela.addCell("Valor");
       
            
            for(Produto prod :objVenda.getProdutos())
            {
                tabela.addCell(Integer.toString(prod.getCodigo()));
                tabela.addCell(prod.getDescricao());
                tabela.addCell(Integer.toString(prod.getQuantidade()));
                tabela.addCell(Double.toString(prod.getValorVenda()));

            }
            PdfPCell total = new PdfPCell(new Paragraph("Total"));
            total.setColspan(3);
            tabela.addCell(total);
            tabela.addCell(Double.toString(objVenda.getValorVenda()));
            
            comprovanteVenda.add(tabela);
            Runtime.getRuntime().exec (new String[]{"cmd.exe", "/c", "start", "C:\\relatoriosBeautySystem\\ComprovanteVenda.pdf"}); 
            }
            catch(DocumentException de){
            de.printStackTrace();
        }catch(IOException ioe){
            ioe.printStackTrace();
        }finally{
            comprovanteVenda.close();
        }
    }
