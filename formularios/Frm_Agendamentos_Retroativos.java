 private void popularAtributos()
    {
        int index = tblresultado.getSelectedRow();
        objAgendamento.setCodigo(agendamentos.get(index).getCodigo());
        objAgendamento.setCliente(agendamentos.get(index).getCliente());
        objAgendamento.setData(agendamentos.get(index).getData());
        objAgendamento.setFuncionario(agendamentos.get(index).getFuncionario());
        objAgendamento.setHoraFinal(agendamentos.get(index).getHoraFinal());
        objAgendamento.setHoraInicial(agendamentos.get(index).getHoraInicial());
        objAgendamento.setValorAgendamento(agendamentos.get(index).getValorAgendamento());
        objAgendamento.setObservacao(agendamentos.get(index).getObservacao());
        servicos = agendamentos.get(index).getServicos();
        objAgendamento.setServicos(servicos);
        objAgendamento.setStatus(agendamentos.get(index).getStatus());
    }
