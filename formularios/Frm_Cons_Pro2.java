    private void bt_inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_inserirActionPerformed
        int index = tblresultado.getSelectedRow();
        objProduto.setCodigo(produtos.get(index).getCodigo());
        objProduto.setDescricao(produtos.get(index).getDescricao());
        objProduto.setGrupoProdutos(produtos.get(index).getGrupoProdutos());
        objProduto.setValorCompra(produtos.get(index).getValorCompra());
        objProduto.setValorVenda(produtos.get(index).getValorVenda());
        if(tipoOperacao == 1)
        {
            Frm_Realizar_Compra f1 = (Frm_Realizar_Compra)parent;
            f1.setProduto(Integer.toString(produtos.get(index).getCodigo()), objProduto);
        f1.setlb_DescPro((produtos.get(index).getDescricao()));
        f1.setTb_ValorItem(Double.toString(produtos.get(index).getValorCompra()).replace(".", ","));
        f1.setVisible(true);
        dispose();
        }
        else if (tipoOperacao == 2)
        {
        Frm_Realizar_Venda f1 = (Frm_Realizar_Venda)parent;
        f1.setProduto(Integer.toString(produtos.get(index).getCodigo()), objProduto);
        f1.setlb_DescPro((produtos.get(index).getDescricao()));
        f1.setTb_ValorItem(Double.toString(produtos.get(index).getValorVenda()).replace(".", ","));
        f1.setVisible(true);
        dispose();
        }
        else if (tipoOperacao == 3)
        {
        Frm_Rel_Movimentacao f1 = (Frm_Rel_Movimentacao)parent;
        f1.setTb_CodiPro(Integer.toString(produtos.get(index).getCodigo()));
        f1.setVisible(true);
        dispose();
        }
        
    }//GEN-LAST:event_bt_inserirActionPerformed

